/**
  * @brief This function handles EXTI line 0 and 1 interrupts.
  */
void EXTI0_1_IRQHandler(void)
{
  /* USER CODE BEGIN EXTI0_1_IRQn 0 */
	mode_status++;
	if(mode_status>2) mode_status=0;
	if(mode_status==0)
	{
		DS1307_setMin(min);
		DS1307_setHour(hours);
	}
  /* USER CODE END EXTI0_1_IRQn 0 */
  HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_0);
  /* USER CODE BEGIN EXTI0_1_IRQn 1 */

  /* USER CODE END EXTI0_1_IRQn 1 */
}

/**
  * @brief This function handles EXTI line 2 and 3 interrupts.
  */
void EXTI2_3_IRQHandler(void)
{
  /* USER CODE BEGIN EXTI2_3_IRQn 0 */
	if(mode_status==1)
	{
		hours++;
		if(hours>24) hours=0;
	}
	else if(mode_status==2)
	{
		min++;
		if(min>60)min=0;
	}
  /* USER CODE END EXTI2_3_IRQn 0 */
  HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_2);
  /* USER CODE BEGIN EXTI2_3_IRQn 1 */

  /* USER CODE END EXTI2_3_IRQn 1 */
}

/**
  * @brief This function handles TIM2 global interrupt.
  */
void TIM2_IRQHandler(void)
{
  /* USER CODE BEGIN TIM2_IRQn 0 */
	/*OFF ALL LED*/
	GPIOC ->ODR = 0XFF;
	GPIOA ->ODR = 0X00;
	/*OFF ALL LED*/
	count++;
	if(count == 1)
	{
		if((mode_status==1)&&(blink==1))
		{
			count++;
		}
		else{
			HAL_GPIO_WritePin(GPIOA,GPIO_PIN_9,1);
			GPIOC ->ODR = led_array[digits[0]];
		}
	}
	else if(count == 2)
	{
		HAL_GPIO_WritePin(GPIOA,GPIO_PIN_10,1);
		GPIOC ->ODR = led_array[digits[1]];
	}
	else if(count == 3)
	{
		if((mode_status==2)&&(blink==1))
		{
			count=0;
		}else{
			HAL_GPIO_WritePin(GPIOA,GPIO_PIN_11,1);
			GPIOC ->ODR = led_array[digits[2]];
		}
	}
	else if(count == 4)
	{
		HAL_GPIO_WritePin(GPIOA,GPIO_PIN_12,1);
		GPIOC ->ODR = led_array[digits[3]];
		count = 0;
	}
  /* USER CODE END TIM2_IRQn 0 */
  HAL_TIM_IRQHandler(&htim2);
  /* USER CODE BEGIN TIM2_IRQn 1 */

  /* USER CODE END TIM2_IRQn 1 */
}

